const mongoose = require('mongoose')

const CotactSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    subject: {
        type: String,
        required: true
    },
    mobile: {
        type: Number,
        required: true
    },
    message: {
        type: String,
        required: true
    }
})

const Contact = mongoose.model('Contact',CotactSchema)
module.exports = Contact;